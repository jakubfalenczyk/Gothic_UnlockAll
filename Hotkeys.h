    std::map<string, int> keyMap;

    bool IsHotkeyTriggered(string& hotkey)
    {
        auto keyIt = keyMap.find(hotkey);

        if (keyIt != keyMap.end()) {
            int mappedKey = keyIt->second;
            bool isKeyToggled = zKeyToggled(mappedKey);

            return isKeyToggled;
        }

        return false;
    }

